// ─── Dunst ───────────────────────────────────────────────────────────────────

fn dunstrc(c: &FessusConfig) -> String {
    let (bg, fg, _) = theme_colors(&c.fessus.theme);
    let origin = dunst_origin(&c.notifications.position);

    format!(
r##"[global]
    font = {font} 11
    origin = {origin}
    notification_limit = {max_visible}
    timeout = {timeout}
    background = "{bg}"
    foreground = "{fg}"
    frame_color = "{accent}"
    frame_width = 2
    corner_radius = 8
    padding = 12
    horizontal_padding = 12
    gap_size = 8
    icon_theme = {icon_theme}
    enable_recursive_icon_lookup = true
    min_icon_size = 32
    max_icon_size = 64

[urgency_low]
    timeout = {timeout}

[urgency_normal]
    timeout = {timeout}

[urgency_critical]
    frame_color = "#ff5555"
    timeout = 0
"##,
        font        = c.fessus.font,
        origin      = origin,
        max_visible = c.notifications.max_visible,
        timeout     = c.notifications.timeout / 1000,
        bg          = bg,
        fg          = fg,
        accent      = c.fessus.accent_color,
        icon_theme  = c.fessus.icon_theme,
    )
}

fn dunst_origin(position: &str) -> &str {
    match position {
        "top-left"     => "top-left",
        "top-right"    => "top-right",
        "bottom-left"  => "bottom-left",
        "bottom-right" => "bottom-right",
        _              => "top-right",
    }
}

// ─── Eww radial menu ─────────────────────────────────────────────────────────

fn eww_yuck(c: &FessusConfig) -> String {
    let anchor = corner_to_eww_anchor(&c.radial.corner);
    let (x, y) = corner_to_xy(&c.radial.corner);

    let app_buttons: String = c.radial.pinned.iter().enumerate()
        .map(|(i, app)| {
            let icon = app_icon(app);
            format!(
                "    (button :class \"app-btn app-btn-{i}\" :onclick \"exec {app}\" \"{icon}\")",
                i = i, app = app, icon = icon,
            )
        })
        .collect::<Vec<_>>()
        .join("\n");

    format!(
r#"(defwindow radial
  :monitor 0
  :geometry (geometry :x "{x}" :y "{y}" :width "280px" :height "280px" :anchor "{anchor}")
  :stacking "overlay"
  :exclusive false
  :visible false
  (radial-menu))

(defwidget radial-menu []
  (box :class "radial-container"
    (button :class "center-btn"
            :onclick "wofi --show drun"
            "")
{app_buttons}
  ))
"#,
        x           = x,
        y           = y,
        anchor      = anchor,
        app_buttons = app_buttons,
    )
}

