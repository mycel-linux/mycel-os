// ─── Helpers ─────────────────────────────────────────────────────────────────

fn theme_colors(theme: &str) -> (&'static str, &'static str, &'static str) {
    match theme {
        "light" => ("#eff1f5", "#4c4f69", "#dce0e8"),
        _       => ("#1e1e2e", "#cdd6f4", "#313244"),
    }
}

fn corner_to_eww_anchor(corner: &str) -> &str {
    match corner {
        "top-left"     => "top left",
        "top-right"    => "top right",
        "bottom-right" => "bottom right",
        _              => "bottom left",
    }
}

fn corner_to_xy(corner: &str) -> (&str, &str) {
    match corner {
        "top-left"     => ("0px", "0px"),
        "top-right"    => ("0px", "0px"),
        "bottom-right" => ("0px", "0px"),
        _              => ("0px", "0px"),
    }
}

