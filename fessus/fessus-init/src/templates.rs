use anyhow::Result;
use std::fs;
use std::path::PathBuf;

use crate::angles;
use crate::schema::FessusConfig;

pub fn generate_all(config: &FessusConfig) -> Result<()> {
    let home = std::env::var("HOME").unwrap_or_else(|_| "/root".to_string());
    let compositor = config.fessus.compositor.as_deref().unwrap_or("sway");

    // Compositor-specific config
    match compositor {
        "hyprland" => {
            write(&home, ".config/hypr/hyprland.conf", &hyprland(config))?;
            write(&home, ".config/hypr/hyprpaper.conf", &hyprpaper(config))?;
        }
        _ => {
            // sway / fessus (default)
            write(&home, ".config/sway/config", &sway(config))?;
        }
    }

    // Shared across all compositors
    write(&home, ".config/waybar/config.jsonc",  &waybar_config(config))?;
    write(&home, ".config/waybar/style.css",     &waybar_style(config))?;
    write(&home, ".config/waybar/launch.sh",     &waybar_launch())?;
    write(&home, ".config/dunst/dunstrc",        &dunstrc(config))?;
    write(&home, ".config/eww/eww.yuck",         &eww_yuck(config))?;
    write(&home, ".config/eww/eww.scss",         &eww_scss(config))?;
    write(&home, ".config/wofi/config",          &wofi_config(config))?;
    write(&home, ".config/wofi/style.css",       &wofi_style(config))?;

    let launch = PathBuf::from(&home).join(".config/waybar/launch.sh");
    use std::os::unix::fs::PermissionsExt;
    let _ = fs::set_permissions(&launch, fs::Permissions::from_mode(0o755));

    Ok(())
}

pub fn print_all(config: &FessusConfig) -> Result<()> {
    println!("=== sway/config ===\n{}", sway(config));
    println!("=== waybar/config.jsonc ===\n{}", waybar_config(config));
    println!("=== eww/eww.yuck ===\n{}", eww_yuck(config));
    Ok(())
}

fn write(home: &str, rel_path: &str, content: &str) -> Result<()> {
    let path = PathBuf::from(home).join(rel_path);
    fs::create_dir_all(path.parent().unwrap())?;
    fs::write(&path, content)?;
    Ok(())
}

// ─── Sway ────────────────────────────────────────────────────────────────────

fn sway(c: &FessusConfig) -> String {
    let mod_key = &c.keybindings.mod_key;

    let launcher_cmd = match c.launcher.provider.as_str() {
        "anyrun" => "anyrun",
        _        => "wofi --show drun",
    };

    let autostart_lines = c.autostart.as_ref()
        .map(|a| a.apps.iter()
            .map(|app| format!("exec_always {}", app))
            .collect::<Vec<_>>()
            .join("\n"))
        .unwrap_or_default();

    let workspace_bindings: String = (1..=c.workspaces.count)
        .map(|i| format!(
            "bindsym ${mod}+{i} workspace number {i}\nbindsym ${mod}+Shift+{i} move container to workspace number {i}",
            mod = "mod", i = i
        ))
        .collect::<Vec<_>>()
        .join("\n");

    let wallpaper = c.fessus.wallpaper.replace(
        "~",
        &std::env::var("HOME").unwrap_or_default(),
    );

    format!(
r#"# Generated by fessus-init — do not edit manually
set $mod {mod_key}
set $term {terminal}
set $launcher {launcher_cmd}

font pango:{font} 10

gaps inner {gaps_inner}
gaps outer {gaps_outer}
default_border pixel {border}

seat seat0 xcursor_theme {cursor_theme} {cursor_size}

exec_always swaybg -i {wallpaper} -m fill
exec_always ~/.config/waybar/launch.sh
exec_always eww daemon

{autostart_lines}

# Core bindings
bindsym $mod+Return exec $term
bindsym $mod+d exec $launcher
bindsym $mod+q kill
bindsym $mod+Shift+r reload
bindsym $mod+f fullscreen toggle
bindsym $mod+v splith
bindsym $mod+s splitv

# Focus
bindsym $mod+h focus left
bindsym $mod+j focus down
bindsym $mod+k focus up
bindsym $mod+l focus right

# Move
bindsym $mod+Shift+h move left
bindsym $mod+Shift+j move down
bindsym $mod+Shift+k move up
bindsym $mod+Shift+l move right

# Session
bindsym $mod+Shift+e exec wlogout 2>/dev/null || swaymsg exit
bindsym $mod+Shift+l exec swaylock -f -c 000000

# Radial menu toggle
bindsym $mod+r exec eww open --toggle radial

{workspace_bindings}

input type:keyboard {{
    xkb_options caps:escape
}}

input type:touchpad {{
    tap enabled
    natural_scroll enabled
}}
"#,
        mod_key         = mod_key,
        terminal        = c.keybindings.terminal,
        launcher_cmd    = launcher_cmd,
        font            = c.fessus.font,
        gaps_inner      = c.window.gaps_inner,
        gaps_outer      = c.window.gaps_outer,
        border          = c.window.border,
        cursor_theme    = c.fessus.cursor_theme,
        cursor_size     = c.fessus.cursor_size,
        wallpaper       = wallpaper,
        autostart_lines = autostart_lines,
        workspace_bindings = workspace_bindings,
    )
}

// ─── Hyprland ─────────────────────────────────────────────────────────────────

fn hyprland(c: &FessusConfig) -> String {
    let mod_key = &c.keybindings.mod_key;

    let autostart_lines = c.autostart.as_ref()
        .map(|a| a.apps.iter()
            .map(|app| format!("exec-once = {}", app))
            .collect::<Vec<_>>().join("\n"))
        .unwrap_or_default();

    let workspace_binds: String = (1..=c.workspaces.count)
        .flat_map(|i| [
            format!("bind = $mod, {i}, workspace, {i}"),
            format!("bind = $mod SHIFT, {i}, movetoworkspace, {i}"),
        ])
        .collect::<Vec<_>>().join("\n");

    let accent_no_hash = c.fessus.accent_color.trim_start_matches('#');
    let wallpaper = c.fessus.wallpaper.replace('~',
        &std::env::var("HOME").unwrap_or_default());

    format!(
r#"# Generated by fessus-init — do not edit manually

monitor = , preferred, auto, 1

$mod      = {mod_key}
$term     = {terminal}
$launcher = wofi --show drun

input {{
    kb_layout  = us
    follow_mouse = 1
    sensitivity  = 0

    touchpad {{
        natural_scroll   = yes
        tap-to-click     = yes
        disable_while_typing = yes
    }}
}}

general {{
    gaps_in  = {gaps_inner}
    gaps_out = {gaps_outer}
    border_size = {border}
    col.active_border   = rgba({accent}ee)
    col.inactive_border = rgba(595959aa)
    layout = dwindle
}}

decoration {{
    rounding = 8
    blur {{
        enabled = yes
        size    = 6
        passes  = 2
    }}
    shadow {{
        enabled = yes
        range   = 8
        render_power = 2
        color   = rgba(00000044)
    }}
}}

animations {{
    enabled = yes
    bezier  = smooth, 0.05, 0.9, 0.1, 1.05
    animation = windows,    1, 6,  smooth
    animation = windowsOut, 1, 5,  default, popin 80%
    animation = fade,       1, 6,  default
    animation = workspaces, 1, 5,  default
}}

dwindle {{
    pseudotile    = yes
    preserve_split = yes
}}

cursor {{
    no_hardware_cursors = false
}}

env = XCURSOR_THEME, {cursor_theme}
env = XCURSOR_SIZE,  {cursor_size}

# Autostart
exec-once = waybar
exec-once = dunst
exec-once = swww-daemon
exec-once = swww img {wallpaper} --transition-type fade
exec-once = eww daemon
{autostart_lines}

# Core bindings
bind = $mod,       Return,  exec, $term
bind = $mod,       D,       exec, $launcher
bind = $mod,       Q,       killactive
bind = $mod,       F,       fullscreen
bind = $mod,       R,       exec, eww open --toggle radial
bind = $mod SHIFT, L,       exec, hyprlock
bind = $mod SHIFT, E,       exec, wlogout 2>/dev/null || hyprctl dispatch exit 0
bind = $mod,       V,       togglefloating

# Focus
bind = $mod, H, movefocus, l
bind = $mod, L, movefocus, r
bind = $mod, K, movefocus, u
bind = $mod, J, movefocus, d

# Move
bind = $mod SHIFT, H, movewindow, l
bind = $mod SHIFT, L, movewindow, r
bind = $mod SHIFT, K, movewindow, u
bind = $mod SHIFT, J, movewindow, d

# Resize with mouse
bindm = $mod, mouse:272, movewindow
bindm = $mod, mouse:273, resizewindow

{workspace_binds}
"#,
        mod_key         = mod_key,
        terminal        = c.keybindings.terminal,
        gaps_inner      = c.window.gaps_inner,
        gaps_outer      = c.window.gaps_outer,
        border          = c.window.border,
        accent          = accent_no_hash,
        cursor_theme    = c.fessus.cursor_theme,
        cursor_size     = c.fessus.cursor_size,
        wallpaper       = wallpaper,
        autostart_lines = autostart_lines,
        workspace_binds = workspace_binds,
    )
}

fn hyprpaper(c: &FessusConfig) -> String {
    let wallpaper = c.fessus.wallpaper.replace('~',
        &std::env::var("HOME").unwrap_or_default());
    format!(
        "preload = {wp}\nwallpaper = ,{wp}\nsplash = false\n",
        wp = wallpaper
    )
}

// ─── Waybar launch script ────────────────────────────────────────────────────

fn waybar_launch() -> String {
    r#"#!/bin/sh
# Kill any existing waybar before relaunching (used by sway reload)
pkill -x waybar 2>/dev/null
sleep 0.1
exec waybar
"#.to_string()
}

// ─── Waybar ───────────────────────────────────────────────────────────────────

fn waybar_config(c: &FessusConfig) -> String {
    let mut right_modules: Vec<&str> = vec![];
    if c.bar.show_bluetooth { right_modules.push("\"bluetooth\""); }
    if c.bar.show_network   { right_modules.push("\"network\""); }
    if c.bar.show_battery   { right_modules.push("\"battery\""); }
    if c.bar.show_tray      { right_modules.push("\"tray\""); }
    right_modules.push("\"clock\"");

    format!(
r#"{{
  "layer": "top",
  "position": "{position}",
  "height": 32,
  "spacing": 4,
  "modules-left": ["sway/workspaces"],
  "modules-center": [],
  "modules-right": [{right}],
  "sway/workspaces": {{
    "disable-scroll": true,
    "all-outputs": true
  }},
  "clock": {{
    "format": "{clock_format}",
    "tooltip": false
  }},
  "battery": {{
    "format": "  {{capacity}}%",
    "format-charging": "  {{capacity}}%"
  }},
  "network": {{
    "format-wifi": "  {{essid}}",
    "format-ethernet": "  connected",
    "format-disconnected": "  offline",
    "tooltip": false
  }},
  "bluetooth": {{
    "format": " ",
    "format-connected": "  {{device_alias}}",
    "tooltip": false
  }},
  "tray": {{
    "spacing": 8
  }}
}}
"#,
        position     = c.bar.position,
        clock_format = c.bar.clock_format,
        right        = right_modules.join(", "),
    )
}

